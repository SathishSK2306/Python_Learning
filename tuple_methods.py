my_tuple = (1, 2, 3, 4, 5, 2, 2)

# 1. count
print(my_tuple.count(2))

# 2. index
print(my_tuple.index(4))


#   other common tuple methods,

my_tuple = (10, 20, 30, 40, 50)

# Length
print(len(my_tuple))

# Maximum
print(max(my_tuple))

# Minimum
print(min(my_tuple))

# Sum
print(sum(my_tuple))

# Slicing
print(my_tuple[1:4])

# Membership
print(30 in my_tuple)
